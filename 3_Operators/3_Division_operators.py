### Division Operators in Python ###

# A Python 3 program to demonstrate use of "/" for both integers and floating points

print ("{} / {} = ".format(5, 2), 5/2)
print ("{} / {} = ".format(-5, 2), -5/2)
print ("{} / {} = ".format(5.0, 2), 5.0/2)
print ("{} / {} = ".format(-5.0, 2), -5.0/2)

print("\n********************************************************************************************************************************")

# Floor division operator:- (//)
print("\nFloor division operator:- (//)\n")

# The real floor division operator is "//". It returns floor value for both integer and
# floating point arguments.

print ("{} // {} = ".format(5, 2), 5//2)
print ("{} // {} = ".format(-5, 2), -5//2)
print ("{} // {} = ".format(5.0, 2), 5.0//2)
print ("{} // {} = ".format(-5.0, 2), -5.0//2)

print("\n********************************************************************************************************************************")